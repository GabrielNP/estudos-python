# Array
lista = [1,2,3,"gabriel",5,1]
print(lista)

# Incluir novo elemento na lista
lista.append("python")
lista.append(20)
print(lista)

# Saber a posição de um elemento na lista
lista.index("gabriel")

# Contar a ocorrência de elementos
lista.count(1)

# Remover um elemento
lista.remove("python")

# Inverter ordem da lista
lista.reverse()

# Ordenar uma lista
lista2 = [4,2,98,7,2,6,7,5]
print(lista2)
lista.sort()